// 获取所有类别
export const getCategories = (language = 'en') => {
  const categories = [...new Set(blogPosts.map(post => language === 'en' ? post.categoryEn : post.category))]
  return categories
}

// 获取特色文章
export const getFeaturedPosts = (language = 'en', limit = 3) => {
  return blogPosts
    .filter(post => post.featured)
    .slice(0, limit)
    .map(post => ({
      ...post,
      title: language === 'en' ? post.titleEn : post.title,
      excerpt: language === 'en' ? post.excerptEn : post.excerpt,
      category: language === 'en' ? post.categoryEn : post.category,
      tags: language === 'en' ? post.tagsEn : post.tags,
      readTime: language === 'en' ? post.readTimeEn : post.readTime
    }))
}

// 根据类别过滤文章
export const getPostsByCategory = (category, language = 'en') => {
  return blogPosts
    .filter(post => (language === 'en' ? post.categoryEn : post.category) === category)
    .map(post => ({
      ...post,
      title: language === 'en' ? post.titleEn : post.title,
      excerpt: language === 'en' ? post.excerptEn : post.excerpt,
      category: language === 'en' ? post.categoryEn : post.category,
      tags: language === 'en' ? post.tagsEn : post.tags,
      readTime: language === 'en' ? post.readTimeEn : post.readTime
    }))
}

// 搜索文章
export const searchPosts = (searchTerm, language = 'en') => {
  return blogPosts
    .filter(post => {
      const title = language === 'en' ? post.titleEn : post.title
      const excerpt = language === 'en' ? post.excerptEn : post.excerpt
      const content = language === 'en' ? post.contentEn : post.content
      
      return title.toLowerCase().includes(searchTerm.toLowerCase()) ||
             excerpt.toLowerCase().includes(searchTerm.toLowerCase()) ||
             content.toLowerCase().includes(searchTerm.toLowerCase())
    })
    .map(post => ({
      ...post,
      title: language === 'en' ? post.titleEn : post.title,
      excerpt: language === 'en' ? post.excerptEn : post.excerpt,
      category: language === 'en' ? post.categoryEn : post.category,
      tags: language === 'en' ? post.tagsEn : post.tags,
      readTime: language === 'en' ? post.readTimeEn : post.readTime
    }))
} 